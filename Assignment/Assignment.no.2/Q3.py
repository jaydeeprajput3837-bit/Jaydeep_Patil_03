#Convert the distant
Feet = int(input("Enter distance in feet: "))
Inches = int(input("Enter distant in inches: "))

#Convert to centimeters
Meter = Feet * 10 + Inches * 5

#Convert to centimeters
Centimeters = Meter * 100

print("Distance in meters =", Meter)
print("Distance in centimeters ", Centimeters)